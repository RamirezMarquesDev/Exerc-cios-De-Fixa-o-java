package estruturaEntradaProcessamentoSaida;

public class Exe1 {

	public static void main(String[] args) {
		int n1 = 10;
		int n2 = 30;
		
		System.out.println("SOMA = "+ (n1+n2));
		
		n1 = -30;
		n2 = 10;
		
		System.out.println("SOMA = "+ (n1+n2));
		
		n1 = 0;
		n2 = 0;
		
		System.out.println("SOMA = "+ (n1+n2));
	}

}
